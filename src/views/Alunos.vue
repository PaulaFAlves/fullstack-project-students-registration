<template>
<div class="alunos">
	<h1 class="subheading">Alunos</h1>
	<v-container class="my-5">
		<v-snackbar v-model="snackbar" top :timeout="4000">
			<span>Aluno inclu√≠do com sucesso!</span>
			<v-btn text color="white" @click="snackbar = false">Close</v-btn>
		</v-snackbar>
		<v-layout row wrap class="mx-0">
			<v-flex md6>
				<v-text-field v-model="search" color="black" label="Pesquisar aluno pelo nome"></v-text-field>
			</v-flex>
			<v-flex md2>
				<v-btn text small @click="searchStudent(search)">Procurar</v-btn>	
			</v-flex>	
		</v-layout>

		<v-divider></v-divider>
		<v-flex class="my-5">
			<IncluirAluno @studentAdded="snackbar = true"/>
		</v-flex>

		<v-layout row class="mb-3">
			<v-btn small text color="grey" @click="sortBy('ra')">
				<v-icon left small>mdi-card-account-details</v-icon>
				<span class="caption text-lowercase">Ordenar por registro</span>
			</v-btn>
			<v-btn small text color="grey" @click="sortBy('name')">
				<v-icon left small>mdi-account</v-icon>
				<span class="caption text-lowercase">Ordenar por nome</span>
			</v-btn>
		</v-layout>

		<v-card flat v-for="data in data" :key="data.id">
			<v-layout row wrap class="mx-3">
				<v-flex xs12 md2>
					<div class="caption grey--text" >Registro:</div>
					<div>{{ data.ra }}</div>
				</v-flex>
				<v-flex xs6 sm4 md6>
					<div class="caption grey--text">Nome:</div>
					<div>{{ data.name }}</div>
				</v-flex>
				<v-flex xs6 sm4 md2>
					<div class="caption grey--text">CPF:</div>
					<div>{{ data.cpf }}</div>
				</v-flex>
				<v-flex xs2 sm4 md2>
					<div class="my-5">
						<EditarAluno 
							:studentId="data.id" 
							:studentName="data.name" 
							:studentEmail="data.email"
							:studentCpf="data.cpf" />
						<ExcluirAluno 
							:studentId="data.id"
							:studentName="data.name" />
					</div>
				</v-flex>
			</v-layout>
			<v-divider></v-divider>
		</v-card>
	</v-container>
</div>
</template>

<script>
import IncluirAluno from '../components/IncluirAluno'
import EditarAluno from '../components/EditarAluno'
import ExcluirAluno from '../components/ExcluirAluno'
import db from '@/db'

export default {
	data() {
	return {
		data: [],
		name: '',
		email: '',
		cpf: '',
		dialog: false,
		dialogEdit: false,
		search: '',
		snackbar: false,
	}
	},
	components: {
		IncluirAluno,
		EditarAluno,
		ExcluirAluno,
	},
	methods: {
		sortBy(prop) {
			this.data.sort((a,b) => a[prop] < b[prop] ? -1 : 1)
		},
		searchStudent(search) {
			db.collection("students").where("name", "==", search)
				.onSnapshot(res => {
					const changes = res.docChanges();
					console.log(changes)
					changes.forEach(change => {
							this.data.push({
								...change.doc.data(),
								// id: change.doc.id
							})
					})
				})
		}
	},
	created() {
		db.collection('students').onSnapshot(res => {
			const changes = res.docChanges();
			console.log(changes)

			changes.forEach(change => {
				if (change.type === 'added'){
					this.data.push({
						...change.doc.data(),
						id: change.doc.id
					})
				}
			})
		})
	}
}
</script>
